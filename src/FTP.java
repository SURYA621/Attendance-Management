    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            String str = "jdbc:mysql://localhost/project";
            Connection x = DriverManager.getConnection(str, "root", "surya");
            String UserName = txt_UserName.getText();
            String PasswordHint = txt_PasswordHint.getText();
            String query = "select * from teacher";
            PreparedStatement ps = x.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            int flag = 0;
            while (rs.next()) {
                
                String Password = rs.getString("password");
                if (rs.getString("teacher_name").equals(UserName) && rs.getString("hint").equals(PasswordHint)) {
                    flag++;

                    txt_Password.setText(Password);
                    JOptionPane.showMessageDialog(this, "Successfully Password retrieved!");
                }

            }
            if (flag == 0) {
                JOptionPane.showMessageDialog(this, "Try again!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

